package fundamentos.secao4;

public class Inferencia {

    public static void main(String[] args) {

        double a = 4.5;
        System.out.println(a);

        var b = 5.4; // o Java define o tipo pelo valor inferido
        System.out.println(b);

        var c = "Texto";
        System.out.println(c);
    }
}
